package org.mycode.structural.adapter;

public class BlackHole {
    public void compressFromSupernova(){
        System.out.println("Black hole has compressed itself from supernova");
    }
    public void absorbAnything(){
        System.out.println("Black hole is absorbing anything");
    }
    public void evaporate(){
        System.out.println("Black hole is evaporating permanently");
    }
}